function valuesInObject(obj) {
  // Your code here
  // once using using a `for...in
  const result = [];
  for (const keys in obj) {
    let values = obj[keys];
    result.push(values);
  }
  console.log(result);

  //once using `Object.values
  const objectResult = Object.values(obj);
  return objectResult;
}



